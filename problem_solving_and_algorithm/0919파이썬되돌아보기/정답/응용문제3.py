# 응용문제3

# 삼각형, 정사각형, 직사각형...넓이

# 삼각형 넓이 : 1/2*밑변*높이
base=int(input('base : '))
height=int(input('height : '))

triArea=1/2*base*height
print('triArea : %d' % triArea)


# 직사각형  넓이 : 가로 * 세로
weight=int(input('weight : '))
height=int(input('height : '))

rectArea=weight * height
print('rectArea : %d' % rectArea)


